// Activity-3/Task 8: Write a program to compare two numbers using > and < and log the result to the console.

// Solution

let num1 = 10;
let num2 = 20;

if (num1 > num2) {
    console.log(num1 + " is greater than " + num2);
} else if (num1 < num2) {
    console.log(num1 + " is less than " + num2);
} else {
    console.log(num1 + " is equal to " + num2);
}

// Output: 10 is greater than 20