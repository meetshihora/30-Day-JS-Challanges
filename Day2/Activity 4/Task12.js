// Activity-4/Task 12: Write a program that uses the || operator to combine two conditions and log the result to the console.

// Solution

let num1 = 10;
let num2 = 20;

if (num1 > 0 || num2 > 0) {
    console.log("At least one number is positive");
} else {
    console.log("Both numbers are negative");
}

// Output: At least one number is positive