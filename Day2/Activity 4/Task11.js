// Activity-4/Task 11: Write a program that uses the && operator to combine two conditions and log the result to the console.

// Solution

let num1 = 10;
let num2 = 20;

if (num1 > 0 && num2 > 0) {
    console.log("Both numbers are positive");
} else {
    console.log("One or both numbers are negative");
}

// Output: Both numbers are positive