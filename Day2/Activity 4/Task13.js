// Activity-4/Task 13: Write a program that uses the ! operator to negate a condition and log the result to the console.

// Solution

let num1 = 10;
let num2 = 20;

if (!(num1 > num2)) {
    console.log(num1 + " is not greater than " + num2);
} else {
    console.log(num1 + " is greater than " + num2);
}

// Output: 10 is not greater than 20