// Activity 2/Task 4: Write a function expression to concatenate two strings and return the result.

// Solution
function concatenateStrings(str1, str2) {
    return str1 + " " + str2;
}

let str1 = "Hello";
let str2 = "World";
let result = concatenateStrings(str1, str2);
console.log(result)

// Output: Hello World