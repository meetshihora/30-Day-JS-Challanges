// Activity 1/Task 2: Wite a function to check if a number is even or ode and log the result to the console.

// Solution

function checkEvenOdd(num) {
    if (num % 2 === 0) {
        console.log("Even");
    } else {
        console.log("Odd");
    }
  } 

checkEvenOdd(10);
checkEvenOdd(11);

// Output: 
    // Even 
    // Odd
 