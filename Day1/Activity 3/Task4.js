// Activity-3/task-4: Create a variable of defferent data types (number, string, boolean, object, array) and log each variable's type using the typeof oprator.

// Number
var myNumber = 42;
console.log(typeof myNumber); 

// String
var myString = "Hello, World!";
console.log(typeof myString);

// Boolean
var myBoolean = true;
console.log(typeof myBoolean);

// Object
var myObject = { name: "John", age: 30 };
console.log(typeof myObject);

// Array
var myArray = [1, 2, 3, 4, 5];
console.log(typeof myArray);

